#!/usr/bin/env python3
"""Generate protocol constant files for ESP32 firmware (C header), Pi
firmware (Python module), and dashboard (JS module) from a single source
of truth.

Source: protocol/constants.json
Outputs:
    firmware/esp32_robot_idf/main/protocol_constants.h — `#define X 500`
    firmware/pi_robot/protocol_constants.py             — `X = 500`
    docs/protocol-constants.js                          — `export const X = 500;`

Each group in the JSON carries a "_targets" list selecting which of the
three outputs it lands in — most constants are firmware-internal (c+py
only); wire-framing values the dashboard also encodes carry js too.

The generated files include a banner that says they're generated; editing
them by hand is wasted effort because the next build will overwrite. Run
manually with `python3 tools/gen-constants.py` or via `make gen-constants`
(which the firmware compile / publish targets depend on).
"""
import json
import os
import sys

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
SOURCE = os.path.join(REPO_ROOT, "protocol", "constants.json")

def banner(comment):
    return (
        f"{comment} Generated by tools/gen-constants.py from protocol/constants.json — do not edit.\n"
        f"{comment} Run `make gen-constants` (or `python3 tools/gen-constants.py`) after editing the JSON.\n"
    )


def flatten(spec, target):
    """Walk {group: {_targets: [...], NAME: value}} into [(group, NAME, value)]
    for groups whose _targets includes `target`."""
    for group, kvs in spec.items():
        if group.startswith("_"):
            continue
        if target not in kvs.get("_targets", []):
            continue
        for name, value in kvs.items():
            if name.startswith("_"):
                continue
            yield group, name, value


def write_if_changed(path, content):
    """No-op write when the file already matches — keeps Make's mtime checks
    happy and avoids spurious commits when nothing actually changed."""
    if os.path.exists(path):
        with open(path) as f:
            if f.read() == content:
                return False
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        f.write(content)
    return True


def gen(spec, path, target, comment, prelude, line):
    """One group-walk generator; the outputs differ only in comment leader,
    prelude, and per-line format."""
    out = [banner(comment), prelude]
    last_group = None
    for group, name, value in flatten(spec, target):
        if group != last_group:
            if last_group is not None:
                out.append("\n")
            out.append(f"{comment} {group}\n")
            last_group = group
        out.append(line(name, value))
    return write_if_changed(path, "".join(out))


def main():
    with open(SOURCE) as f:
        spec = json.load(f)
    targets = [
        ("firmware/esp32_robot_idf/main/protocol_constants.h",
         "c",  "//", "#pragma once\n\n", lambda n, v: f"#define {n:24} {v}\n"),
        ("firmware/pi_robot/protocol_constants.py",
         "py", "#",  "\n",               lambda n, v: f"{n} = {v}\n"),
        ("docs/protocol-constants.js",
         "js", "//", "\n",               lambda n, v: f"export const {n} = {v};\n"),
    ]
    changed = []
    for rel, target, comment, prelude, line in targets:
        path = os.path.join(REPO_ROOT, rel)
        if gen(spec, path, target, comment, prelude, line):
            changed.append(rel)
    if changed:
        for c in changed:
            print(f"updated {c}")
    else:
        print("constants: up to date")


if __name__ == "__main__":
    sys.exit(main())
